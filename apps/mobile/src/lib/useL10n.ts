export function useL10n() {
  const { user } = useAuth();
  const normalized = String(user?.preferred_language || '').trim().toLowerCase();
  const language: UiLanguage =
    normalized === 'chinese'
      ? 'chinese'
      : normalized === 'french'
        ? 'french'
        : normalized === 'spanish'
          ? 'spanish'
          : normalized === 'german'
            ? 'german'
            : normalized === 'japanese'
              ? 'japanese'
              : normalized === 'korean'
                ? 'korean'
                : normalized === 'portuguese'
                  ? 'portuguese'
                  : normalized === 'arabic'
                    ? 'arabic'
                    : normalized === 'hindi'
                      ? 'hindi'
          : 'english';
  const isChinese = language === 'chinese';

  function t(english: string, chinese: string) {
    if (language === 'chinese') return chinese;
    if (language === 'french') return fr[english] || english;
    if (language === 'spanish') return es[english] || english;
    if (language === 'german') return de[english] || english;
    if (language === 'japanese') return ja[english] || english;
    if (language === 'korean') return ko[english] || english;
    if (language === 'portuguese') return pt[english] || english;
    if (language === 'arabic') return ar[english] || english;
    if (language === 'hindi') return hi[english] || english;
    return english;
  }

  return { t, isChinese, language };
}
